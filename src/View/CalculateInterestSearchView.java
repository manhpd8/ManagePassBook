    private void jBtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSearchActionPerformed

        if (jTFSearchInput.getText().equals("")) {
            JOptionPane.showMessageDialog(this, StringUtility.NOT_EMPTY_SEARCH_KEY_INPUT);
        } else {
            String string = jTFSearchInput.getText();
            try {
                Double num = Double.parseDouble(string);
            } catch (NumberFormatException e) {
                System.out.println("khong phai la so");
                JOptionPane.showMessageDialog(this, StringUtility.INPUT_ONLY_NUMBER);
                return;
            }
            int type = 0;
            if (jRBCMND.isSelected()) {
                type = 1;
            } else {
                type = 0;
            }
            list = new CalculateInterestDAO().search(jTFSearchInput.getText().trim(), type);
            if(list.isEmpty()){
                JOptionPane.showMessageDialog(this, StringUtility.RESULT_EMPTY);
            }
            loadList();
            jTbSearchResults.setVisible(true);
        }
    }//GEN-LAST:event_jBtnSearchActionPerformed

