    private void jBtnXemLaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnXemLaiActionPerformed
        PassBookSearch pbs = new PassBookSearch();
        int rowselect = jTbSearchResults.getSelectedRow();
        pbs.setCMTND((String) jTbSearchResults.getValueAt(rowselect, 1));
        pbs.setGoc(Double.parseDouble((String) jTbSearchResults.getValueAt(rowselect, 3)));
        pbs.setKyhan((int) jTbSearchResults.getValueAt(rowselect, 5));
        pbs.setLaiXuat((double) jTbSearchResults.getValueAt(rowselect, 6));
        pbs.setLoaiSo((String) jTbSearchResults.getValueAt(rowselect, 4));
        pbs.setMaSOTietKiem((int) jTbSearchResults.getValueAt(rowselect, 2));
        pbs.setSoTaiKhoan((int) jTbSearchResults.getValueAt(rowselect, 0));
        pbs.setNgayMo((Timestamp) jTbSearchResults.getValueAt(rowselect, 7));

        new CalculateInterestResultView(pbs).setVisible(true);

    }//GEN-LAST:event_jBtnXemLaiActionPerformed

